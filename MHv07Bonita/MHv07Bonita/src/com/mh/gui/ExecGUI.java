    public void refrescarTabla() {
        ArrayList<Helado> al = mh.toList();
        DefaultTableModel m = (DefaultTableModel) this.heladosTable.getModel();
        m.setNumRows(0);
        for (Helado h : al) {
            Object[] o = {h.getPosicion(), h.getNombre(), String.format("%.02f€",h.getPrecio()), h.getTipo(), h.getCantidad()};
            m.addRow(o);
        }
    }

