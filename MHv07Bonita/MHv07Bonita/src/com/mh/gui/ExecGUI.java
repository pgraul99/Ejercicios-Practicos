    private void pedirButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pedirButtonActionPerformed
        reproducirSonidoBoton();
        Helado conseguido = null;
        String pos = this.posicionTextField.getText();
        //DefaultTableModel m = (DefaultTableModel) this.heladosTable.getModel();
        //JOptionPane.showMessageDialog(this, "Aqui tienes tu " + m.getColumnName(3));
        try {
            // TODO add your handling code here:           
            conseguido = mh.pedirHelado(pos);
            //mh.setMonedero(mh.getMonedero());
            this.dineroTextField.setText(String.valueOf(mh.getMonedero()));
            reproducirSonidoCompra();
            JOptionPane.showMessageDialog(this, "Aquí tienes tu helado: " + conseguido.getNombre() + " , no se olvide del cambio " + mh.getMonedero() + " €");
            mh.setMonedero(0);
            this.dineroTextField.setText("0.00 €");
            refrescarTabla();
        } catch (Exception ex) {
            //Tipos de mensajes que se muestran
//            JOptionPane.showMessageDialog(null, "Este es un mensaje de advertencia", "Advertencia", JOptionPane.WARNING_MESSAGE);
//            JOptionPane.showMessageDialog(null, "Este es un mensaje de información", "Información", JOptionPane.INFORMATION_MESSAGE);
//            JOptionPane.showMessageDialog(null, "Este es un mensaje de error", "Error", JOptionPane.ERROR_MESSAGE);
//            JOptionPane.showMessageDialog(null, "Este es un mensaje simple", "Mensaje", JOptionPane.PLAIN_MESSAGE);
            reproducirSonidoAlerta();
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Advertencia", JOptionPane.WARNING_MESSAGE);

        }

    }//GEN-LAST:event_pedirButtonActionPerformed

